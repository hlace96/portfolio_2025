    <script>
      document.addEventListener("DOMContentLoaded", function() {
        const portfolioContainer = document.querySelector(".row.boxes");
        const portfolioItems = Array.from(portfolioContainer.children);

        function shuffle(array) {
          for (let i = array.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [array[i], array[j]] = [array[j], array[i]];
          }
        }
        shuffle(portfolioItems);
        portfolioContainer.innerHTML = "";
        portfolioItems.forEach(item => portfolioContainer.appendChild(item));
      });
    </script> 
